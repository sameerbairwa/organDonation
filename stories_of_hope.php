<div class="data"><b>Name:</b> Nozer H Canteenwalla<br>
<b>Age at interview :</b> 48 years<br>
<b>Sex :</b> Male<br>
<b>Background:</b> For more than eight years, Nozer H Canteenwalla endured dialysis treatments three times a week, as he was suffering from end-stage renal disease (ESRD). See how he was given a second chance at life after he received a cadaver organ transplant.


</div>
<!-- <div class="imag"><img src="dddd.jpg" align="right" class="userimage"><br><br></div> -->
<p>
<br>
<br>
<br>
<br>
<br>
<br>
<b>When did you first realize that you were not well?</b><br><br>

I was diagnosed with polycystic kidney disorder around June 2003. Actually, it is a hereditary disease from my mother’s side. I lost my grandmother to it and even my mother underwent dialysis for almost two years. As it was a hereditary problem, I underwent a test, which was positive. However, the kidneys were functioning properly and hence there was nothing to worry. But gradually, as I was unable to take proper care of my kidneys, the condition of the kidneys deteriorated when I was just 38.
One day I came from office and realized that I had not used the bathroom the whole day. So I got my blood tests done and came to know that my kidneys had given up completely. As it was a family condition, I knew what to expect.<br><br>

<b>What was the initial prognosis by the doctor?</b><br><br>

I had an infection in my kidneys and the doctor to whom I had gone first was not able to treat it. Due to this, the condition of the kidneys deteriorated even more. Actually, if the kidneys function normally, one could lead a healthy life for 30-40 years even if one has polycystic kidneys. But I had a severe infection due to eating out often. Also, the doctor could not control the infection and I was directly put on dialysis. I took a discharge from that hospital and consulted Dr. Bharat Shah of Narmada Kidney Foundation.<br><br>

<b>What was the treatment suggested to you by the doctor initially?</b><br><br>
Dr. Bharat Shah was able to bring down the creatinine level of the kidney and thus control it’s functioning through proper diet and just a few medicines.<br><br>

<b>When were you told you would need an organ transplant?</b><br><br>

Even with the medicines I was taking and the diet I was following, the condition of my kidneys deteriorated and Dr. Shah asked me to be ready for dialysis. He also asked me to register my name for cadaver organ transplant. As I did not have any family member who could donate a kidney, this was the only option for me. As the awareness about cadaver organ donation in India was very less, it was best to go on dialysis as soon as possible because people who go on dialysis sooner and then go for transplant have a better chance of recovery than those who delay dialysis and go for transplant. I strictly followed everything that Dr. Shah suggested and started my dialysis. I had to undergo dialysis three times a week and I never missed any one session in eight years.<br><br>
<b>How were you enrolled into the list to receive an organ?</b><br><br>

I had to fill up a form and register with the hospital. Once it was registered, the only thing to be done was to wait.<br><br>

<b>Did you get any indication as to how long the wait would be?</b><br><br>
No. There was no such indication. It all depended on the blood group match and also there was not much awareness at that time, so the when part could not be said.<br><br>

<b>What kind of emotional support did you receive at that time and from whom?</b><br><br>

To be very honest, I did not need any emotional support. On the first day I met Dr. Shah, I told him that I am alone. If you are there with me, I am ready to face the facts. I want to be a winner in life. These were my words to him and he replied that we would manage. From that day, there was no looking back. I did not bother to ask for help from anybody. The team of Narmada Kidney Foundation was my support system, my morale booster. But I fought the battle all alone. There was no one to take me to the hospital for the transplant. Honestly, I was focused. As I had lost my mother because I could not find an organ donor for her, I had decided that I would not give up till I get an organ. I was sure to succeed in this. I knew that I was in good hands. So I never felt the need for emotional support.<br><br>

<b>Did you have any contact with people who had been through something similar before?</b><br><br>

No. I did not know who had undergone a similar situation before the transplant.<br><br>

<b>How long did you actually have to wait for the transplant?</b><br><br>
I waited eight years for the transplant.<br><br>


<b>Would you like to say anything to the people who are reading this interview?</b><br><br>
I feel sad that one and half lakh people are waiting for an organ today. The message that I would like to give is that if you cannot do any good deed during your life, at least after you die, do this for a good cause. Donate your organs after you die; you have nothing to lose. You have your whole life to lead it the way you want to. You can be bad, good, happy, sad, make money, do whatever you want while you are alive; the choice is yours. But after you die, don’t let people bury you or burn you before your organs are donated to those who need it. Save lives so that someone can lead a long and happy life.


</p>	




